void	init_int_to_zero(int *nb1, int *nb2, int *nb3, int *nb4)
{
	if (nb1)
		*nb1 = 0;
	if (nb2)
		*nb2 = 0;
	if (nb3)
		*nb3 = 0;
	if (nb4)
		*nb4 = 0;
}
